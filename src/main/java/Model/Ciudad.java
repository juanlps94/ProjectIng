package Model;

/**
 *
 * @author Juan
 */
public class Ciudad {
    String name;
    String zona;
    String rank;
    String info;
    String opinionC;
    String conjuntoR;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getZona() {
        return zona;
    }

    public void setZona(String zona) {
        this.zona = zona;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getOpinionC() {
        return opinionC;
    }

    public void setOpinionC(String opinionC) {
        this.opinionC = opinionC;
    }

    public String getConjuntoR() {
        return conjuntoR;
    }

    public void setConjuntoR(String conjuntoR) {
        this.conjuntoR = conjuntoR;
    }
    
}
